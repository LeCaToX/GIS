@app.post("/api/provinces/{name}/process")
async def start_processing(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404, f"Province '{name}' not found")
    p = _pdir(matched)
    cmd = f'python pipeline.py --province "{matched}"'
    if p.exists():
        cmd += " --resolution 5"
    return JSONResponse(
        status_code=405,
        content={
            "status": "disabled",
            "message": (
                "Pipeline execution from website is disabled. "
                "Run pipeline locally/offline, then refresh website."
            ),
            "command": cmd,
        },
    )


