@app.get("/api/provinces/{name}/boundary")
async def province_boundary(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404, f"Province '{name}' not found")
    pdir = _pdir(matched)
    out = {"boundary": None, "internal": None}
    bp = pdir / "boundary.geojson"
    if bp.exists():
        boundary_json = json.loads(bp.read_text(encoding="utf-8"))
        try:
            gdf_b = gpd.GeoDataFrame.from_features(boundary_json["features"], crs="EPSG:4326")
            gdf_b["geometry"] = gdf_b.geometry.apply(_strip_holes)
            out["boundary"] = json.loads(gdf_b.to_json())
        except Exception:
            out["boundary"] = boundary_json
    ibp = pdir / "internal_boundaries.geojson"
    if ibp.exists():
        out["internal"] = json.loads(ibp.read_text(encoding="utf-8"))
    if out["boundary"] is None:
        raise HTTPException(404, "Boundary not found – run pipeline first")
    return out


@app.get("/api/provinces/{name}/layers")
async def province_layers(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404)
    pdir = _pdir(matched)
    if not pdir.exists():
        return {"raster": [], "vector": []}

    rasters = []
    for lyr in ["hillshade", "dem", "slope", "aspect", "landcover"]:
        for sub in ["", "native"]:
            p = pdir / sub / f"{lyr}.tif" if sub else pdir / f"{lyr}.tif"
            if p.exists():
                rasters.append(lyr)
                break

    vectors = []
    for lyr, ext in [("infrastructure", ".gpkg"), ("contour", ".shp")]:
        if (pdir / f"{lyr}{ext}").exists():
            vectors.append(lyr)

    return {"raster": rasters, "vector": vectors}


