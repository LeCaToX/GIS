def _safe(name: str) -> str:
    return name.replace(" ", "_").replace(".", "")

def _pdir(name: str) -> Path:
    return DATA_DIR / _safe(name)

def _collected_dir_for_province(name: str) -> Optional[Path]:
    mapped = COLLECTED_PROVINCE_MAP.get(name)
    if not mapped:
        return None
    p = COLLECTED_DIR / mapped
    return p if p.exists() else None

def _resolve_raster_path(pdir: Path, province_name: str, layer: str) -> Optional[Path]:
    tif = pdir / f"{layer}.tif"
    if not tif.exists():
        tif = pdir / "native" / f"{layer}.tif"
    if tif.exists():
        return tif
    cdir = _collected_dir_for_province(province_name)
    if cdir:
        tif2 = cdir / f"{layer}.tif"
        if tif2.exists():
            return tif2
    return None

def _resolve_vector_path(pdir: Path, province_name: str, layer: str) -> Optional[Path]:
    ext_map = {"roads": ".gpkg", "rivers": ".gpkg",
               "infrastructure": ".gpkg", "contour": ".shp",
               "real_ls_point": ".shp"}
    ext = ext_map.get(layer)
    if not ext:
        return None
    fp = pdir / f"{layer}{ext}"
    if fp.exists():
        return fp
    cdir = _collected_dir_for_province(province_name)
    if cdir:
        fp2 = cdir / f"{layer}{ext}"
        if fp2.exists():
            return fp2
    return None

def _resolve(query: str) -> Optional[str]:
    """Match a query string to a canonical 2025 province name."""
    q = _normalize_vn(query)
    for new_name in PROVINCE_MERGER_2025:
        if _normalize_vn(new_name) == q or q in _normalize_vn(new_name):
            return new_name
    for new_name, old_names in PROVINCE_MERGER_2025.items():
        for old in old_names:
            if _normalize_vn(old) == q or q in _normalize_vn(old):
                return new_name
    return None

def _gso(name: str) -> dict:
    old_names = PROVINCE_MERGER_2025.get(name, [])
    total_pop, total_area = 0, 0.0
    constituents = []
    for old in old_names:
        d = GSO_PROVINCE_DATA.get(old)
        if d:
            p, a = d["population"], d["area_km2"]
            total_pop += p
            total_area += a
            constituents.append({"name": old, "population": p,
                                 "area_km2": a,
                                 "density_per_km2": round(p / a, 1) if a else 0})
    return {
        "name": name, "population": total_pop,
        "area_km2": round(total_area, 2),
        "density_per_km2": round(total_pop / total_area, 1) if total_area else 0,
        "constituents": constituents, "is_merged": len(old_names) > 1,
    }


def _strip_holes(geom):
    """Remove interior holes/slivers from polygons for cleaner web rendering."""
    if geom is None or geom.is_empty:
        return geom
    if isinstance(geom, Polygon):
        return Polygon(geom.exterior)
    if isinstance(geom, MultiPolygon):
        polys = [Polygon(p.exterior) for p in geom.geoms if not p.is_empty]
        return MultiPolygon(polys) if polys else geom
    if isinstance(geom, GeometryCollection):
        polys = []
        for g in geom.geoms:
            if isinstance(g, Polygon):
                polys.append(Polygon(g.exterior))
            elif isinstance(g, MultiPolygon):
                polys.extend([Polygon(p.exterior) for p in g.geoms if not p.is_empty])
        return MultiPolygon(polys) if polys else geom
    return geom

# ── GADM loader (cached) ──────────────────────────────
_gadm_gdf = None
_boundaries_cache = None

def _load_gadm() -> gpd.GeoDataFrame:
    global _gadm_gdf
    if _gadm_gdf is not None:
        return _gadm_gdf
    GADM_CACHE.parent.mkdir(parents=True, exist_ok=True)
    if not GADM_CACHE.exists():
        for p in DATA_DIR.glob("*/raw/gadm41_VNM_1.json"):
            shutil.copy2(p, GADM_CACHE)
            break
        else:
            download_file(GADM_URL, GADM_CACHE, "GADM Vietnam")
    if not GADM_CACHE.exists():
        raise RuntimeError("Cannot load GADM boundary data")
    _gadm_gdf = gpd.read_file(GADM_CACHE)
    return _gadm_gdf

def _build_merged_boundaries():
    """Build 34-province merged GeoJSON (cached)."""
    global _boundaries_cache
    if _boundaries_cache is not None:
        return _boundaries_cache
    gdf = _load_gadm()
    features = []
    for new_name, old_names in sorted(PROVINCE_MERGER_2025.items()):
        parts = []
        for old in old_names:
            q = _normalize_vn(old)
            normed = gdf["NAME_1"].apply(_normalize_vn)
            m = gdf[normed == q]
            if not m.empty:
                parts.append(m.iloc[0].geometry)
        if not parts:
            continue
        geom = unary_union(parts) if len(parts) > 1 else parts[0]
        try:
            geom = geom.buffer(0)
        except Exception:
            pass
        geom = _strip_holes(geom)
        gs = gpd.GeoSeries([geom], crs=gdf.crs).to_crs("EPSG:4326")
        geojson_geom = json.loads(gs.to_json())["features"][0]["geometry"]
        gso = _gso(new_name)
        features.append({
            "type": "Feature",
            "geometry": geojson_geom,
            "properties": {
                "name": new_name,
                "population": gso["population"],
                "area_km2": gso["area_km2"],
                "density_per_km2": gso["density_per_km2"],
                "is_merged": gso["is_merged"],
                "processed": (_pdir(new_name) / "boundary.geojson").exists(),
            },
        })
    _boundaries_cache = {"type": "FeatureCollection", "features": features}
    return _boundaries_cache

# ── Auto-scale: percentile-based vmin/vmax ─────────────

def _get_layer_range_for_tif(pdir: Path, layer: str, tif: Path) -> Tuple[float, float]:
    """Return (vmin, vmax) for a raster layer, preferring sidecar stats JSON."""
    safe_name = pdir.name
    key = (safe_name, layer)
    if key in _stats_cache:
        return _stats_cache[key]

    cfg = LAYER_CONFIG.get(layer)
    fallback = (cfg["vmin"], cfg["vmax"]) if cfg else (0, 1)

    stats_file = tif.with_suffix(".stats.json")
    if stats_file.exists():
        try:
            s = json.loads(stats_file.read_text())
            # Stable rounding keeps LRU tile cache keys consistent across requests.
            result = (round(float(s["vmin"]), 4), round(float(s["vmax"]), 4))
            _stats_cache[key] = result
            return result
        except Exception:
            pass

    try:
        with rasterio.open(tif) as src:
            data = src.read(1).astype(float)
            nd = src.nodata
        if nd is not None:
            data[data == nd] = np.nan
        valid = data[~np.isnan(data)]
        if len(valid) == 0:
            _stats_cache[key] = fallback
            return fallback
        vmin = round(float(np.percentile(valid, 2)), 4)
        vmax = round(float(np.percentile(valid, 98)), 4)
        if vmax - vmin < 1e-6:
            vmin, vmax = fallback
        _stats_cache[key] = (vmin, vmax)
        return (vmin, vmax)
    except Exception:
        _stats_cache[key] = fallback
        return fallback


def _get_layer_range(pdir: Path, layer: str) -> Tuple[float, float]:
    """Backwards-compatible wrapper for province rasters."""
    tif = pdir / f"{layer}.tif"
    if not tif.exists():
        tif = pdir / "native" / f"{layer}.tif"
    if not tif.exists():
        cfg = LAYER_CONFIG.get(layer)
        return (cfg["vmin"], cfg["vmax"]) if cfg else (0, 1)
    return _get_layer_range_for_tif(pdir, layer, tif)


# ── Tile renderer ──────────────────────────────────────

_LC_LUT = np.zeros((256, 4), dtype=np.uint8)
for _cls_id, (_, _, _hex) in LANDCOVER_CLASSES.items():
    if _cls_id < 256:
        _LC_LUT[_cls_id] = [int(_hex[1:3], 16), int(_hex[3:5], 16),
                            int(_hex[5:7], 16), 220]

_CLSI_LUT = np.zeros((256, 4), dtype=np.uint8)
# 1-5: rất thấp, thấp, trung bình, cao, rất cao
_CLSI_COLORS = {
    1: "#2c7bb6",  # very low (blue)
    2: "#abd9e9",  # low (light blue)
    3: "#ffffbf",  # medium (yellow)
    4: "#fdae61",  # high (orange)
    5: "#d7191c",  # very high (red)
}
for _k, _hex in _CLSI_COLORS.items():
    _CLSI_LUT[_k] = [int(_hex[1:3], 16), int(_hex[3:5], 16),
                     int(_hex[5:7], 16), 230]

def _render_tile(data: np.ndarray, mask: np.ndarray, layer: str,
                 vmin: float = 0, vmax: float = 1) -> bytes:
    """Colorize a single-band tile array to RGBA PNG bytes."""
    h, w = data.shape

    if layer == "landcover":
        idx = np.clip(data.astype(int), 0, 255)
        rgba = _LC_LUT[idx].copy()
        rgba[mask == 0, 3] = 0
    elif layer == "c_lsi":
        idx = np.clip(data.astype(int), 0, 255)
        rgba = _CLSI_LUT[idx].copy()
        rgba[mask == 0, 3] = 0
    else:
        cfg = LAYER_CONFIG.get(layer, {"cmap": "viridis"})
        cmap = mpl_cm.get_cmap(cfg["cmap"])
        normed = np.clip((data - vmin) / max(vmax - vmin, 1e-6), 0, 1)
        rgba = (cmap(normed) * 255).astype(np.uint8)
        rgba[..., 3] = np.where(mask > 0, 220, 0)

    buf = io.BytesIO()
    Image.fromarray(rgba, "RGBA").save(buf, "PNG")
    return buf.getvalue()

