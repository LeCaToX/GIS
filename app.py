@app.get("/api/provinces/{name}/boundary")
async def province_boundary(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404, f"Province '{name}' not found")
    pdir = _pdir(matched)
    out = {"boundary": None, "internal": None}
    bp = pdir / "boundary.geojson"
    if bp.exists():
        boundary_json = json.loads(bp.read_text(encoding="utf-8"))
        try:
            gdf_b = gpd.GeoDataFrame.from_features(boundary_json["features"], crs="EPSG:4326")
            gdf_b["geometry"] = gdf_b.geometry.apply(_strip_holes)
            out["boundary"] = json.loads(gdf_b.to_json())
        except Exception:
            out["boundary"] = boundary_json
    ibp = pdir / "internal_boundaries.geojson"
    if ibp.exists():
        out["internal"] = json.loads(ibp.read_text(encoding="utf-8"))
    if out["boundary"] is None:
        raise HTTPException(404, "Boundary not found – run pipeline first")
    return out


@app.get("/api/provinces/{name}/layers")
async def province_layers(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404)
    pdir = _pdir(matched)
    if not pdir.exists():
        return {"raster": [], "vector": []}

    rasters = []
    for lyr in ["hillshade", "dem", "slope", "aspect", "landcover", "p_lsi", "c_lsi"]:
        if _resolve_raster_path(pdir, matched, lyr):
            rasters.append(lyr)

    vectors = []
    for lyr in ["infrastructure", "contour", "real_ls_point"]:
        if _resolve_vector_path(pdir, matched, lyr):
            vectors.append(lyr)

    return {"raster": rasters, "vector": vectors}


@app.get("/api/provinces/{name}/stats")
async def province_stats(name: str):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404)
    pdir = _pdir(matched)

    result = _gso(matched)

    rp = pdir / "socioeconomic_report.json"
    if rp.exists():
        result["socioeconomic"] = json.loads(rp.read_text(encoding="utf-8"))

    gp = pdir / "gso_population.json"
    if gp.exists():
        result["gso_detail"] = json.loads(gp.read_text(encoding="utf-8"))

    return result


@app.get("/api/provinces/{name}/vector/{layer}")
async def province_vector(name: str, layer: str,
                          simplify: float = Query(0.0015),
                          max_features: int = Query(20000, ge=500, le=100000)):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404)
    pdir = _pdir(matched)

    fp = _resolve_vector_path(pdir, matched, layer)
    if not fp:
        raise HTTPException(404, f"Unknown or missing vector layer: {layer}")

    gdf = gpd.read_file(fp)
    if gdf.crs and str(gdf.crs) != "EPSG:4326":
        gdf = gdf.to_crs("EPSG:4326")
    if simplify > 0:
        gdf["geometry"] = gdf.geometry.simplify(simplify)
    gdf = gdf[gdf.geometry.notnull()].copy()

    # Do not expose precise landslide coordinates. Reduce precision of geometries
    # before serializing (approx ~110m at 0.001 degrees).
    if layer == "real_ls_point":
        def _round_point_geom(geom):
            try:
                if geom is None or geom.is_empty:
                    return geom
                # Shapely Point
                x, y = geom.x, geom.y
                return type(geom)(round(float(x), 3), round(float(y), 3))
            except Exception:
                return geom
        gdf["geometry"] = gdf.geometry.apply(_round_point_geom)

    # Large province-wide OSM layers can freeze the browser if sent raw.
    if len(gdf) > max_features:
        stride = max(1, int(math.ceil(len(gdf) / max_features)))
        gdf = gdf.iloc[::stride].copy()

    return JSONResponse(json.loads(gdf.to_json()))


@app.get("/api/provinces/{name}/tiles/{layer}/{z}/{x}/{y}.png")
def tile(name: str, layer: str, z: int, x: int, y: int):
    matched = _resolve(name)
    if not matched:
        raise HTTPException(404)

    pdir = _pdir(matched)
    headers = {"Cache-Control": "public, max-age=86400"}

    cache_path = pdir / "tiles" / layer / str(z) / str(x) / f"{y}.png"
    tif = _resolve_raster_path(pdir, matched, layer)
    if not tif:
        return Response(content=_empty_tile(), media_type="image/png")

    if z < 0 or z > _MAX_TILE_ZOOM:
        return Response(content=_empty_tile(), media_type="image/png")

    if _png_tile_cache_valid(cache_path, tif):
        return FileResponse(cache_path, media_type="image/png", headers=headers)

    try:
        vmin, vmax = _get_layer_range_for_tif(pdir, layer, tif)
        content = _cached_tile_render(str(tif), layer, z, x, y, vmin, vmax, matched)

        try:
            cache_path.parent.mkdir(parents=True, exist_ok=True)
            cache_path.write_bytes(content)
        except Exception:
            pass

        return Response(content=content, media_type="image/png", headers=headers)
    except Exception:
        return Response(content=_empty_tile(), media_type="image/png")


